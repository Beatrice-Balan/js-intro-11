// WHILE loop
// checks the condition first and then runs the block of code

let num1 = 0;

while(num1 < 3) {
    console.log(num1);
    num1++
}

console.log('End of the program')

// DO WHILE loop
// do-while executes the block of code first and then checks the condition

let num2 = 0;

do {
    console.log(num2);
    num2++;
} while(num2 < 0)
    console.log('End of the program!')